-- Add RFC field to profiles table
ALTER TABLE public.profiles 
  ADD COLUMN rfc TEXT;

-- Update the trigger function to include RFC
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
BEGIN
  INSERT INTO public.profiles (id, first_name, last_name, rfc)
  VALUES (
    new.id, 
    new.raw_user_meta_data ->> 'first_name', 
    new.raw_user_meta_data ->> 'last_name',
    new.raw_user_meta_data ->> 'rfc'
  );
  RETURN new;
END;
$$;